# Text ko paragraphs mein divide karein
sentences = content.strip().split('\n\n')  # Paragraph ke hisaab se divide ho jayega

all_filtered_tokens = []
for sentence in sentences:
    normalized_sentence = normalize(sentence)
    tokens = word_tokenizer(normalized_sentence)
    filtered_tokens = [word for word in tokens if word not in STOP_WORDS]
    all_filtered_tokens.extend(filtered_tokens)

print("Filtered Tokens:", all_filtered_tokens)

